/**
 * Cosine similarity between two equal-length numeric vectors.
 *
 * Returns a value in [-1, 1]. Throws `Error` when the input dimensions differ
 * (a bug in the caller — silently returning 0 would mask it).
 *
 * Returns 0 if either vector has zero magnitude (degenerate input).
 */
export function cosineSimilarity(a: number[], b: number[]): number {
  if (a.length !== b.length) {
    throw new Error(
      `cosineSimilarity: vector length mismatch (${a.length} vs ${b.length})`,
    );
  }

  let dot = 0;
  let magA = 0;
  let magB = 0;
  for (let i = 0; i < a.length; i++) {
    dot += a[i] * b[i];
    magA += a[i] * a[i];
    magB += b[i] * b[i];
  }

  if (magA === 0 || magB === 0) return 0;

  return dot / (Math.sqrt(magA) * Math.sqrt(magB));
}
